public class Calculator {
	
	int x;
	int y;

	public Calculator() {
	
	}

	public int subtract(int minuend, int subtrahend){
		return minuend - subtrahend;
	}
	
	public int divide(int x, int y) {
		return x / y;
	}

	public int multiplication(int x, int y) {
		return x * y;
	}

	public int addition(int x, int y) {
		return x + y;
	}
}
